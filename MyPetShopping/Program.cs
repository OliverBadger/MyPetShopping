//        defaults: new { controller = "About", action = "Index" });

//app.MapControllerRoute(
//    name: "company",
//    pattern: "company",
//    defaults: new { controller = "Company", action = "Index" });

/* Both of these are added using attribution in the controller classes */

app.MapRazorPages();

app.Run();
